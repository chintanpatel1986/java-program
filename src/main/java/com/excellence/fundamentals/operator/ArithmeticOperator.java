package com.excellence.fundamentals.operator;

public class ArithmeticOperator {
    public static void main(String[] args) {

        int numberOne = 100;
        int numberTwo = 60;

        System.out.println(numberOne + numberTwo);
        System.out.println(numberOne - numberTwo);
        System.out.println(numberOne * numberTwo);
        System.out.println(numberOne / numberTwo);
        System.out.println(numberOne % numberTwo);
    }
}