package com.excellence.fundamentals.operator;

public class RelationalOperator {
    public static void main(String[] args) {

        int numberOne = 10;
        int numberTwo = 60;

        System.out.println("(numberOne < numberTwo)  : " + (numberOne < numberTwo));
        System.out.println("(numberOne > numberTwo)  : " + (numberOne > numberTwo));
        System.out.println("(numberOne <= numberTwo) : " + (numberOne <= numberTwo));
        System.out.println("(numberOne >= numberTwo) : " + (numberOne >= numberTwo));
        System.out.println("(numberOne == numberTwo) : " + (numberOne == numberTwo));
        System.out.println("(numberOne != numberTwo) : " + (numberOne != numberTwo));
    }
}
