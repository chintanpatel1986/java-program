package com.excellence.fundamentals.operator;

public class AssignmentOperator {
    public static void main(String[] args) {

        int number = 20;

        number += 10;
        System.out.println(number);

        number -= 5;
        System.out.println(number);

        number *= 2;
        System.out.println(number);

        number /= 5;
        System.out.println(number);

        number %= 3;
        System.out.println(number);
    }
}
